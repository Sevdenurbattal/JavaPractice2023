package day04_concatenation;

public class PrimitiveTypeCastings {
    public static void main(String[] args) {
        //-----Implicit Casting-----
        byte a = 15;
        short b = (short)  a ;
        System.out.println(b);

        int c = a; //implicit casting
        long d = 3000L;

        float f= d; //implicit casting


        //---explicit casting----
        int x=100;
        byte y = (byte)  x;




    }



}
